// ===============================================================================
// LOAD DATA
// ===============================================================================

var friends = require("../data/friends");

// ===============================================================================
// ROUTING
// ===============================================================================

module.exports = function(app) {

    app.get("/api/home", function(req, res) {
        res.json(friends);
    });
      
    app.post("/api/home", function(req, res) {
        friends.push(req.body);
        res.json(true);
    });
};
